
public class AndOperatorExamples {

	public static void main(String[] args) {
		 char ans='y';
		 int count =2;
		 
		 if(ans=='y' & count==0) {
			 System.out.println("count is zero.");
		 }
		 if(ans=='y'&count==1) {
			 System.out.println("count is one");
		 }
		 if(ans=='y'&count==2) {
			 System.out.println("count is two");
		 }

	}

}
