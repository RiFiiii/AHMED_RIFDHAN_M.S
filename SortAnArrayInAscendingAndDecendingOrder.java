package assignment2;

public class SortAnArrayInAscendingAndDecendingOrder {

	
	public static void main(String[] args) {
		
		System.out.print("Ascending Order: ");
		for(int i=1;i<=10;i++) {
			 
			 System.out.print("  "+i);
		 }
		System.out.println();
		System.out.print("Descending Order.:");
		
		for(int j=10;j>0;--j) {
			System.out.print("  "+j);
		}
		}
	}

