package javaGoldenLaps;

public class EsacapeSequenceDemo {

	public static void main(String[] args) {
		System.out.println("hello\"eve\"world");
		 //using escape sequence backslash here
		 System.out.println("hello\'eve\'world");
		 //single quote acceptable
		 System.out.println("hello\n eve\"world");
		 //new line
		 System.out.println("hello\n\teve\n\tworld");
		 //new line + tab space
		 System.out.println("hello\f world");
		 //page.break
		 System.out.println("hello\b\b\b world");
		 System.out.println("hello\r world");
		 //carriage return

	}

}
